# Use regex to find each article and insert title
def add_title(match):
    num_str = match.group(1)
    num = int(num_str)
    rest = match.group(2)
    title = titles.get(num, f"第{num}条")
    return f"{{ number: {num}, title: \"{title}\",{rest}"

# Pattern: `{ number: 123, ...}`
pattern = r"\{\s*number:\s*(\d+),([^}]*?paragraphs:\s*\[.*?\]\s*)\}"
# Use DOTALL for multi-line matching
result = re.sub(pattern, add_title, content, flags=re.DOTALL)

with open("src/data/law-articles.ts", "w") as f:
    f.write(result)

